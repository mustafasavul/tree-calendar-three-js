export function isRTL(langCode) {
  const rtlLanguages = ['ar', 'he', 'fa', 'ur'];
  return rtlLanguages.includes(langCode);
}

/**
 * Get locale string for date formatting
 * @param {string} langCode - Language code
 * @returns {string} Locale string for Intl.DateTimeFormat
 */
export function getLocale(langCode) {
  const localeMap = {
    en: 'en-US',
    tr: 'tr-TR',
    de: 'de-DE',
    fr: 'fr-FR',
    es: 'es-ES',
    it: 'it-IT',
    pt: 'pt-BR',
    ru: 'ru-RU',
    ja: 'ja-JP',
    zh: 'zh-CN',
    ko: 'ko-KR',
    ar: 'ar-SA'
  };
  
  return localeMap[langCode] || 'en-US';
}

